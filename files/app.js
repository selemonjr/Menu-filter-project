const filterBtn = document.querySelectorAll(".filter-btn");
const selection = document.querySelector(".selection-center");
window.addEventListener("DOMContentLoaded",() => {
    displayMenuItems(menu);
});
filterBtn.forEach((btn) => {
    btn.addEventListener("click",(e) => {
        const category = e.currentTarget.dataset.id;
        const categoryMenu = menu.filter((menuItem)=> {
            if(menuItem.category === category) {
                return menuItem
            }
        });
        if(category === "all") {
            displayMenuItems(menu);
        }
        else {
            displayMenuItems(categoryMenu)
        }
    });
});
function displayMenuItems(menuItems) {
    let displayMenu = menuItems.map((item) => {
        return `<div class="item-1">
        <img src=${item.img} alt="">
    </div>
    <div class="item-2">
        <div class="text">
            <h3 id="food">${item.food}</h3>
            <h3 id="price">${item.price}</h3>
        </div>
        <span id="line"></span>
        <div class="information">
            <p id="info">${item.info}</p>
        </div>
    </div>`;
    });
    displayMenu = displayMenu.join("");
    selection.innerHTML = displayMenu
};
